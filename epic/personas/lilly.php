	<h1>Lilly Poblano</h1>
		<p>Age: 23</p>
		<p>Gender: Female</p>
		<h2>User Story:</h2>
			<p>Lilly is a 2nd class private in the US army who is deployed to Afghanistan. She is trying to figure out a way
			to get supplies that she needs/wants.</p>

	<h2>Background: </h2>
		<p>Lilly is has been deployed to Afghanistan for 2 months and is planed to be deployed for 6 months total. She is single,
		and has no kids. Her only close family relation back home is her father, who is unwell. Lilly was born and raised in Spokane, Washington. This
		is her 2nd deployment. She finds herself missing the small comforts of home like her favorite candy, red licorice. She wishes she
		had a new book to read on her off time and a deck of cards to play with her platoon. Plus she needs more stationary.</p>

	<h2>Frustrations:</h2>
		<p>Her father is the only one in her family that can send supplies to her, but is sick.
		She has limited access to a computer with internet. Is able to use a computer on base.</p>

	<h2>Technology:</h2>
		<p>She has a cell-phone which is a iphone 6s that has the sim card changed, but she can only use this when she is not out on a mission, or on duty. The
		Computers on base are Dell Inspiron 22 3000 All-in-One with Windows 10 Home.</p>

	<h2>Goals:</h2>
		<p>Lilly is needs to find another way to get supplies. She would like to find a way to get exactly what she needs.</p>

